// ============ HELPER FUNCTIONS ============

export function getAcademyById(id: string): Academy | undefined {
  return academies.find(a => a.id === id);
}

export function getCourseById(id: string): Course | undefined {
  return courses.find(c => c.id === id);
}

export function getCoursesForJob(jobId: string): Course[] {
  return courses.filter(c => c.targetJobIds.includes(jobId));
}

export function getCoursesByCategory(category: CourseCategory): Course[] {
  return courses.filter(c => c.category === category);
}

export function getCoursesByAcademy(academyId: string): Course[] {
  return courses.filter(c => c.academyId === academyId);
}

export function getPartnerAcademies(): Academy[] {
  return academies.filter(a => a.isPartner);
}

export function getFreeCourses(): Course[] {
  return courses.filter(c => c.price === 0);
}

export function getCoursesWithTag(tag: string): Course[] {
  return courses.filter(c => c.tags.includes(tag));
}

// Format price display
export function formatCoursePrice(price: number): string {
  if (price === 0) {
    return 'ë¬´ë£Œ';
  }
  return `${price.toLocaleString()}ë§Œì›`;
}

// Get popular courses (by enrollment)
export function getPopularCourses(limit: number = 5): Course[] {
  return [...courses]
    .sort((a, b) => (b.enrollCount || 0) - (a.enrollCount || 0))
    .slice(0, limit);
}

// Get highly rated courses
export function getTopRatedCourses(limit: number = 5): Course[] {
  return [...courses]
    .sort((a, b) => (b.rating || 0) - (a.rating || 0))
    .slice(0, limit);
}

// Search courses
export function searchCourses(query: string): Course[] {
  const lowerQuery = query.toLowerCase();
  return courses.filter(c =>
    c.title.toLowerCase().includes(lowerQuery) ||
    c.description.toLowerCase().includes(lowerQuery) ||
    c.tags.some(t => t.toLowerCase().includes(lowerQuery))
  );
}
